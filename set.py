set1={10,20,30,40,50} #union
set2={10,20,60,70,80}
print(set1.union(set2))

set1={10,20,30,40,50} #intersection
set2={10,20,60,70,80}
print(set1.intersection(set2))

set1={10,20,30,40,50} #difference
set2={10,20,60,70,80}
print(set1.difference(set2))

set1={10,20,30,40,50} #symmetric difference
set2={10,20,60,70,80}
print(set1.symmetric_difference(set2))


